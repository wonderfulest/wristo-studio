export function translate(key: string, locale: SupportedLocale, params?: Record<string, string | number>): string {
  let message = messages[locale]?.[key] || (en as Record<string, string>)[key] || key
  if (params) {
    Object.entries(params).forEach(([name, value]) => {
      message = message.split(`{${name}}`).join(String(value))
    })
  }
  return message
}

export function useI18n() {
  const localeStore = useLocaleStore()
  const locale = computed(() => localeStore.currentLocale)
  const t = (key: string, params?: Record<string, string | number>) => translate(key, locale.value, params)
  return { locale, t }
}
